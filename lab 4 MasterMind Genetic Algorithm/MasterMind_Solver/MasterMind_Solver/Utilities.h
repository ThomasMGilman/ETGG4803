	template<typename T>
	T roulette_wheel_selection(vector<T> data, T maxVal)
	{
		quickSort(data, 0, data.size() - 1);
		reverse(data.begin(), data.end() - 1);
		while (true)
		{
			float U = get_random_float();
			for (T val : data)
			{
				if (((float)val / (float)maxVal) >= U)
					return val;
			}
		}
	}

