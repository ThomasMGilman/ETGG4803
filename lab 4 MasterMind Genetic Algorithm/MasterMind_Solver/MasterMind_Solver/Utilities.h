	template<typename T>
	int get_match_count(vector<T>& a, vector<T>& b)
	{
		int matches = 0;
		if (a.size() != b.size())
			throw new exception("Mismatched list sizes given, need to give equally sized lists");
		for (int i = 0; i < a.size(); i++)
			matches += a[i] == b[i] ? 1 : 0;
		return matches;
	};

